function actuator = b777_actuator_model()
%B777_ACTUATOR_MODEL First-cut B777-like control-surface actuator model.
%
% The model defines position limits, rate limits and first-order response
% constants for the plant-facing elevator, effective aileron and rudder states.

actuator = struct();
actuator.id = "B777_ACTUATOR_MODEL_V0";
actuator.name = "B777-like control-surface actuator seed";
actuator.data_status = "first-cut engineering estimates for plant integration";
actuator.surface_names = ["elevator"; "aileron"; "rudder"];
actuator.units.angle = "rad";
actuator.units.angular_rate = "rad/s";
actuator.units.time = "s";

actuator.elevator = make_surface( ...
    "elevator", ...
    "delta_e_rad", ...
    "delta_e_cmd_rad", ...
    deg2rad_local(-25.0), ...
    deg2rad_local(25.0), ...
    deg2rad_local(-20.0), ...
    deg2rad_local(20.0), ...
    0.20, ...
    "positive trailing-edge down; expected Cm_delta_e < 0");

actuator.aileron = make_surface( ...
    "effective aileron", ...
    "delta_a_rad", ...
    "delta_a_cmd_rad", ...
    deg2rad_local(-20.0), ...
    deg2rad_local(20.0), ...
    deg2rad_local(-30.0), ...
    deg2rad_local(30.0), ...
    0.15, ...
    "positive effective command: left aileron down, right aileron up");

actuator.rudder = make_surface( ...
    "rudder", ...
    "delta_r_rad", ...
    "delta_r_cmd_rad", ...
    deg2rad_local(-30.0), ...
    deg2rad_local(30.0), ...
    deg2rad_local(-20.0), ...
    deg2rad_local(20.0), ...
    0.25, ...
    "positive command produces positive yawing moment");

actuator.notes = { ...
    'This is an integration model, not a certified flight-control actuator deck.', ...
    'Effective aileron is a single aggregate roll-control input used by the aerodynamic database.', ...
    'Surface deflections are saturated before rate limiting; rate commands are prevented from driving farther into a position limit.' ...
};
end

function surface = make_surface(name, stateField, commandField, ...
    positionMin_rad, positionMax_rad, rateMin_radps, rateMax_radps, ...
    tau_s, convention)

surface = struct();
surface.name = name;
surface.state_field = stateField;
surface.command_field = commandField;
surface.position_min_rad = positionMin_rad;
surface.position_max_rad = positionMax_rad;
surface.position_min_deg = rad2deg_local(positionMin_rad);
surface.position_max_deg = rad2deg_local(positionMax_rad);
surface.rate_min_radps = rateMin_radps;
surface.rate_max_radps = rateMax_radps;
surface.rate_min_degps = rad2deg_local(rateMin_radps);
surface.rate_max_degps = rad2deg_local(rateMax_radps);
surface.tau_s = tau_s;
surface.model = "rate-limited first-order lag";
surface.convention = convention;
end

function value_rad = deg2rad_local(value_deg)
value_rad = value_deg * pi / 180.0;
end

function value_deg = rad2deg_local(value_rad)
value_deg = value_rad * 180.0 / pi;
end
