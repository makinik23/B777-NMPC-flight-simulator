aero.primary_model = ...
    'NASA CRM NTF197 clean Mach-alpha grid with DATCOM-ready residual derivatives and CRM-HL/DATCOM-ready high-lift increments';
aero.units.angle = 'rad';
aero.units.rate = 'rad/s';
aero.units.coefficient = '-';

aero.reference.S_m2 = geom.wing.area_m2;
aero.reference.b_m = geom.wing.span_m;
