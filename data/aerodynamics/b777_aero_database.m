function coeff = database_coeff(aero, base, state, control, varargin)
validate_state_control(state, control);

if nargin < 5 || isempty(varargin)
    configName = "clean";
else
    configName = string(varargin{1});
end

cfg = find_config(aero.configs, configName);
coeff = base.coeff(state, control);

[coeff, baselineInfo] = apply_clean_cruise_table(aero, coeff, state, control);
baseCL = coeff.CL;

coeff.CL = coeff.CL + cfg.increments.CL0;
coeff.CD = coeff.CD ...
    + cfg.increments.CD0 ...
    + cfg.increments.induced_drag_delta * max(coeff.CL^2 - baseCL^2, 0.0);
coeff.Cm = coeff.Cm + cfg.increments.Cm0;

coeff.config = cfg.name;
coeff.config_status = cfg.status;
coeff.primary_source_id = cfg.primary_source_id;
coeff.current_source_id = baselineInfo.current_source_id;
coeff.clean_baseline_source_id = baselineInfo.current_source_id;
coeff.config_increment_source_id = cfg.current_source_id;
coeff.residual_derivative_source_id = baselineInfo.residual_source_id;
coeff.CLmax_estimate = cfg.CLmax_estimate;
coeff.CL_margin_to_estimate = cfg.CLmax_estimate - coeff.CL;
coeff.flap_deg = cfg.flap_deg;
coeff.slat_deployed = cfg.slat_deployed;
coeff.gear_down = cfg.gear_down;
coeff.table_status = baselineInfo.status;
coeff.confidence = min([cfg.confidence, baselineInfo.confidence]);
coeff.validity_status = check_validity(aero, state);
end

function [coeff, info] = apply_clean_cruise_table(aero, coeff, state, control)
info.current_source_id = "INITIAL_ANALYTIC_SEED";
info.status = "fallback-analytic-seed";
info.residual_source_id = "INITIAL_ANALYTIC_SEED";
info.confidence = 0.25;

tableData = aero.tables.clean_cruise;
if ~is_clean_table_eligible(tableData, state)
    return;
end

tableCoeff = lookup_clean_table(tableData, state.Mach, state.alpha_rad);
residualCoeff = derivative_residual_coeff( ...
    aero.tables.derivative_seed, state, control, aero.reference);

coeff.CL = tableCoeff.CL + residualCoeff.CL;
coeff.CD = tableCoeff.CD + residualCoeff.CD;
coeff.Cm = tableCoeff.Cm + residualCoeff.Cm;
coeff.CY = residualCoeff.CY;
coeff.Cl = residualCoeff.Cl;
coeff.Cn = residualCoeff.Cn;
coeff.p_hat = residualCoeff.p_hat;
coeff.q_hat = residualCoeff.q_hat;
coeff.r_hat = residualCoeff.r_hat;

info.current_source_id = tableData.id;
info.status = tableData.status;
info.residual_source_id = aero.tables.derivative_seed.id;
info.confidence = min([tableData.confidence, aero.tables.derivative_seed.confidence]);
end

function residual = derivative_residual_coeff(derivatives, state, control, reference)
V = max(state.V_mps, 1.0);
pHat = state.p_radps * reference.b_m / (2.0 * V);
qHat = state.q_radps * reference.c_m / (2.0 * V);
rHat = state.r_radps * reference.b_m / (2.0 * V);

beta = state.beta_rad;
de = control.delta_e_rad;
da = control.delta_a_rad;
dr = control.delta_r_rad;

residual = struct();
residual.CL = get_derivative(derivatives, 'dynamic', 'CL_q') * qHat ...
    + get_derivative(derivatives, 'control', 'CL_delta_e') * de;
residual.CD = get_derivative(derivatives, 'static', 'CD_beta') * beta^2 ...
    + get_derivative(derivatives, 'control', 'CD_delta_e') * de^2 ...
    + get_derivative(derivatives, 'control', 'CD_delta_a') * da^2 ...
    + get_derivative(derivatives, 'control', 'CD_delta_r') * dr^2;
residual.Cm = get_derivative(derivatives, 'dynamic', 'Cm_q') * qHat ...
    + get_derivative(derivatives, 'control', 'Cm_delta_e') * de;
residual.CY = get_derivative(derivatives, 'static', 'CY_beta') * beta ...
    + get_derivative(derivatives, 'dynamic', 'CY_p') * pHat ...
    + get_derivative(derivatives, 'dynamic', 'CY_r') * rHat ...
    + get_derivative(derivatives, 'control', 'CY_delta_a') * da ...
    + get_derivative(derivatives, 'control', 'CY_delta_r') * dr;
residual.Cl = get_derivative(derivatives, 'static', 'Cl_beta') * beta ...
    + get_derivative(derivatives, 'dynamic', 'Cl_p') * pHat ...
    + get_derivative(derivatives, 'dynamic', 'Cl_r') * rHat ...
    + get_derivative(derivatives, 'control', 'Cl_delta_a') * da ...
    + get_derivative(derivatives, 'control', 'Cl_delta_r') * dr;
residual.Cn = get_derivative(derivatives, 'static', 'Cn_beta') * beta ...
    + get_derivative(derivatives, 'dynamic', 'Cn_p') * pHat ...
    + get_derivative(derivatives, 'dynamic', 'Cn_r') * rHat ...
    + get_derivative(derivatives, 'control', 'Cn_delta_a') * da ...
    + get_derivative(derivatives, 'control', 'Cn_delta_r') * dr;
residual.p_hat = pHat;
residual.q_hat = qHat;
residual.r_hat = rHat;
end

function value = get_derivative(derivatives, groupName, coefficientName)
if ~isfield(derivatives, groupName) ...
        || ~isfield(derivatives.(groupName), coefficientName)
    error('b777_aero_database:MissingDerivative', ...
        'Missing aerodynamic derivative %s.%s.', groupName, coefficientName);
end

value = derivatives.(groupName).(coefficientName);
end

function tf = is_clean_table_eligible(tableData, state)
if ~isfield(state, 'Mach') || ~isnumeric(state.Mach) ...
        || ~isscalar(state.Mach) || ~isfinite(state.Mach)
    tf = false;
    return;
end

tf = state.Mach >= tableData.validity.Mach(1) ...
    && state.Mach <= tableData.validity.Mach(2) ...
    && state.alpha_rad >= tableData.validity.alpha_rad(1) ...
    && state.alpha_rad <= tableData.validity.alpha_rad(2) ...
    && state.beta_rad >= tableData.validity.beta_rad(1) ...
    && state.beta_rad <= tableData.validity.beta_rad(2);
end

function tableCoeff = lookup_clean_table(tableData, mach, alphaRad)
tableCoeff = struct();
tableCoeff.CL = interp2( ...
    tableData.grid.Mach, ...
    tableData.grid.alpha_rad, ...
    tableData.grid.CL, ...
    mach, ...
    alphaRad, ...
    'linear');
tableCoeff.CD = interp2( ...
    tableData.grid.Mach, ...
    tableData.grid.alpha_rad, ...
    tableData.grid.CD, ...
    mach, ...
    alphaRad, ...
    'linear');
tableCoeff.Cm = interp2( ...
    tableData.grid.Mach, ...
    tableData.grid.alpha_rad, ...
    tableData.grid.Cm, ...
    mach, ...
    alphaRad, ...
    'linear');
end

function validate_state_control(state, control)
requiredState = ["V_mps", "alpha_rad", "beta_rad", ...
    "p_radps", "q_radps", "r_radps"];
requiredControl = ["delta_e_rad", "delta_a_rad", "delta_r_rad"];

for k = 1:numel(requiredState)
    field = char(requiredState(k));
    if ~isfield(state, field) || ~isnumeric(state.(field)) ...
            || ~isscalar(state.(field)) || ~isfinite(state.(field))
        error('b777_aero_database:InvalidState', ...
            'state.%s must be a finite numeric scalar.', field);
    end
end

for k = 1:numel(requiredControl)
    field = char(requiredControl(k));
    if ~isfield(control, field) || ~isnumeric(control.(field)) ...
            || ~isscalar(control.(field)) || ~isfinite(control.(field))
        error('b777_aero_database:InvalidControl', ...
            'control.%s must be a finite numeric scalar.', field);
    end
end
end

function status = check_validity(aero, state)
limits = aero.validity.seed_model;

inAlpha = state.alpha_rad >= limits.alpha_rad(1) ...
    && state.alpha_rad <= limits.alpha_rad(2);
inBeta = state.beta_rad >= limits.beta_rad(1) ...
    && state.beta_rad <= limits.beta_rad(2);

if isfield(state, 'Mach') && isnumeric(state.Mach) && isfinite(state.Mach)
    inMach = state.Mach >= limits.Mach(1) && state.Mach <= limits.Mach(2);
else
    inMach = true;
end

if inAlpha && inBeta && inMach
    status = "inside-seed-validity";
else
    status = "outside-seed-validity";
end
end

function cfg = find_config(configs, configName)
for k = 1:numel(configs)
    if configs(k).name == configName
        cfg = configs(k);
        return;
    end
end

error('b777_aero_database:UnknownConfig', ...
    'Unknown aerodynamic configuration: %s.', char(configName));
end

